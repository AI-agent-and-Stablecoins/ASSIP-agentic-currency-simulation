"""Experiment 005: LLM model comparison (GPT vs Claude vs Gemini).

Research question: do agents reasoning via different LLMs (GPT, Claude,
Gemini) converge on the same currency preferences, or does model choice
itself bias medium-of-exchange selection?

src/llm/llm_router.py and src/llm/agent_reasoning.py are now real
OpenRouter-backed implementations (not Phase 2 stubs) -- see those modules
and tests/test_llm.py. Running this experiment for real requires:
    1. OPENROUTER_API_KEY set in the environment (https://openrouter.ai/keys)
    2. Outbound network access to https://openrouter.ai

If either isn't available, this script explains exactly what's missing and
exits non-zero -- it does not fabricate a comparison. (Live OpenRouter calls
were not exercised while building this experiment, since openrouter.ai isn't
reachable from that sandbox; tests/test_llm.py verifies the request/response
contract with a mocked session instead. This script should be run for real
in an environment with API access.)

Protocol (once credentials/network are available):
    1. Build a buyer_prompt (src/llm/prompts/buyer_prompt.txt) describing a
       sample consumer's wallet and the candidate (currency, chain) options
       generated by the same routing_engine.generate_candidates() the
       rule-based agents use.
    2. Send that identical prompt to gpt, claude, and gemini via
       generate_reasoning(), routed through src/llm/llm_router.py.
    3. Parse each model's CURRENCY/CHAIN/OFFER_PRICE response and compare.
    4. Score each model's OFFER_PRICE against the good's true_price using
       src/llm/hallucination_detector.py's overpayment_pct().
"""

import os
import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from experiments._common import (  # noqa: E402
    load_simulation_config,
    make_run_dir,
    print_header,
    print_kv,
    save_json,
)
from src.agents.agent_factory import build_agent, load_agent_profiles  # noqa: E402
from src.blockchain.chain import load_chain_universe  # noqa: E402
from src.blockchain.routing_engine import generate_candidates  # noqa: E402
from src.currencies.currency import load_currency_universe  # noqa: E402
from src.llm.agent_reasoning import build_prompt, generate_reasoning  # noqa: E402
from src.llm.hallucination_detector import overpayment_pct  # noqa: E402
from src.market.goods import Good  # noqa: E402

EXPERIMENT_NAME = "experiment_005_model_comparison"
MODELS_TO_COMPARE = ("gpt", "claude", "gemini")
SAMPLE_GOOD = Good(name="cloud_compute", category="cloud_compute", base_price_usd=100.0)


def _candidates_summary(candidates) -> str:
    lines = [
        f"- {c.currency_symbol} on {c.chain_name}: governance={c.governance_score:.2f}, "
        f"liquidity={c.liquidity_score:.2f}, gas_fee=${c.gas_fee:.2f}, "
        f"compliant={c.genius_compliant}"
        for c in candidates
    ]
    return "\n".join(lines)


def _parse_response(text: str) -> dict:
    fields = {}
    for key in ("CURRENCY", "CHAIN", "OFFER_PRICE"):
        match = re.search(rf"{key}:\s*(.+)", text)
        fields[key.lower()] = match.group(1).strip() if match else None
    return fields


def main() -> None:
    print_header("Experiment 005: Model comparison (GPT vs Claude vs Gemini)")

    if not os.environ.get("OPENROUTER_API_KEY"):
        print(
            "OPENROUTER_API_KEY is not set. This experiment makes real calls to OpenRouter "
            "(src/llm/agent_reasoning.py) and needs a real key -- get one at "
            "https://openrouter.ai/keys, then:\n\n"
            "    export OPENROUTER_API_KEY=sk-or-...\n"
            "    python experiments/experiment_005_model_comparison.py\n"
        )
        raise SystemExit(1)

    currencies = load_currency_universe()
    chains = load_chain_universe()
    profiles = load_agent_profiles()
    sample_buyer = build_agent(profiles["consumer"])
    candidates = generate_candidates(sample_buyer.wallet.balances, currencies, chains)

    prompt = build_prompt(
        "buyer",
        agent_id=sample_buyer.agent_id,
        risk_profile=sample_buyer.risk_profile,
        wallet_summary=", ".join(f"{sym}: {amt:.2f}" for sym, amt in sample_buyer.wallet.balances.items()),
        good_name=SAMPLE_GOOD.name,
        true_price=SAMPLE_GOOD.base_price_usd,
        candidates_summary=_candidates_summary(candidates),
    )

    results = {}
    for model in MODELS_TO_COMPARE:
        print_kv("model", model)
        raw_response = generate_reasoning(prompt, agent_class="buyer", model_key=model)
        parsed = _parse_response(raw_response)
        offer_price = float(parsed["offer_price"]) if parsed.get("offer_price") else None
        overpayment = overpayment_pct(SAMPLE_GOOD.base_price_usd, offer_price) if offer_price else None
        results[model] = {"raw_response": raw_response, **parsed, "overpayment_pct": overpayment}
        print_kv(f"  {model} choice", {"currency": parsed.get("currency"), "chain": parsed.get("chain")})
        print_kv(f"  {model} overpayment_pct", overpayment)

    chosen_currencies = {model: r.get("currency") for model, r in results.items()}
    print_kv("chosen_currencies", chosen_currencies)
    print_kv("models_agree", len(set(chosen_currencies.values())) == 1)

    paths = make_run_dir(EXPERIMENT_NAME)
    save_json(
        paths["metrics"] / "model_comparison.json",
        {
            "prompt": prompt,
            "results": results,
            "chosen_currencies": chosen_currencies,
        },
    )
    print(f"\nSaved run artifacts to {paths['base']}")


if __name__ == "__main__":
    main()
